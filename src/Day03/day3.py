# Arithmetic operators:

a = int(input("Enter first value: "))
b = int(input("Enter second value: "))

print("first value is:", a , "| second value is:", b)

add = a + b
sub = a - b
mult = a * b
div = a / b
mod = a % b

print("=======================================")
print("Sum of above two values: ", add)
print("Substraction of above two values: ", sub)
print("Multiplication of above two values: ", mult)
print("Divison of above two values: ", div)
print("Modulo of above two values: ", mod)
print("=======================================")
